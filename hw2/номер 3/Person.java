public class Person {
    String name;
    String surname;
    int weight;
    int high;
    String gender;
    int salary;
    boolean majority;
    Person(String a, String b, int c, int d, String e, int f, boolean g){
        name = a;
        surname = b;
        weight = c;
        high = d;
        gender = e;
        salary = f;
        majority = g;
    }
}
